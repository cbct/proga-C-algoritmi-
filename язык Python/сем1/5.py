l = [1,2,3,4]
for i in range(len(l)):       # цикл с перебиранием списка L
 a = l[i] + l[i-1]            # ПРИМЕР--- элемент 0 (это цифра 1) и отнимаем на 1 элемента назад (это число 4)
 print(a)                     # получается 1 + 4 = 5


